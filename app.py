from flask import Flask, render_template, request, jsonify
from flask_cors import CORS
from dotenv import load_dotenv
import os
import google.generativeai as genai
import re
import traceback

load_dotenv()

app = Flask(__name__, static_folder='static', template_folder='templates')
CORS(app)

# ‚úÖ Gemini setup with recommended stable model
genai.configure(api_key=os.getenv("GEMINI_API_KEY"))
model = genai.GenerativeModel("gemini-1.5-pro-latest")

# ‚úÖ Utility to cap scores at 10
def cap_scores(text):
    return re.sub(r'(\d{2,})/10', lambda m: f"{min(int(m.group(1)), 10)}/10", text)

@app.route("/")
def home():
    return render_template("index.html")

@app.route("/review", methods=["POST"])
def review():
    try:
        data = request.get_json()
        prd_text = data.get("prd_text", "").strip()

        if not prd_text:
            return jsonify({"response": "‚ùå No PRD text provided."}), 400

        prompt = f"""
        You are an expert PRD reviewer. Strictly follow the format below.

        Return all scores as numbers from 0 to 10 only. Use this exact structure and headings ‚Äî do not change them. Fill every field.

        ===
        **1. Summary:**  
        [Short one-paragraph summary]

        **2. Scores:**  
        Clarity: [0-10]  
        Structure: [0-10]  
        Completeness: [0-10]  
        Ambiguity: [0-10]  
        Stakeholder Consideration: [0-10]  
        Technical Depth: [0-10]  
        Feasibility: [0-10]  
        Business Impact Alignment: [0-10]

        **3. Strengths:**  
        - Bullet point 1  
        - Bullet point 2  

        **4. Areas for Improvement:**  
        - Bullet point 1  
        - Bullet point 2  
        ===

        Now analyze the PRD below accordingly:

        \"\"\"
        {prd_text}
        \"\"\"
        """

        response = model.generate_content(prompt)

        if response.text:
            print("‚úÖ Gemini Raw Response:\n", response.text)
            safe_text = cap_scores(response.text)
            return jsonify({"response": safe_text})
        else:
            print("‚ö†Ô∏è Gemini returned empty response")
            return jsonify({"response": "‚ùå No response generated by Gemini."}), 500

    except Exception as e:
        print("üî• Error:", e)
        traceback.print_exc()
        return jsonify({"response": f"‚ùå Server error: {str(e)}"}), 500
